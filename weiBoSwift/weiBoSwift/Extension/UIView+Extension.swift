import UIKit

extension UIView {
    
    // 宽度
    var width: CGFloat {
        return self.frame.size.width
    }
    
    // 高度
    var height: CGFloat {
        return self.frame.size.height
    }
    
    // Y
    var X: CGFloat {
        return self.frame.origin.x
    }
    
    // Y
    var Y: CGFloat {
        return self.frame.origin.x
    }
    
}
